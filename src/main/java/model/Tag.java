package model;

public class Tag {
	private int tagno;
    private String tag;
    private int movieno;
    
	public int getTagno() {
		return tagno;
	}
	public void setTagno(int tagno) {
		this.tagno = tagno;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public int getMovieno() {
		return movieno;
	}
	public void setMovieno(int movieno) {
		this.movieno = movieno;
	}
}
