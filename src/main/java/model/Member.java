package model;

public class Member {
	private int memberno;
	private String id;
	private String password;
	private String nickname;
	private String netflix;
	private String disney;
	private String coupang;
	private String tving;
	private String memberout;
	


	public int getMemberno() {
		return memberno;
	}

	public void setMemberno(int memberno) {
		this.memberno = memberno;
	}


	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getNetflix() {
		return netflix;
	}

	public void setNetflix(String netflix) {
		this.netflix = netflix;
	}

	public String getDisney() {
		return disney;
	}

	public void setDisney(String disney) {
		this.disney = disney;
	}

	public String getCoupang() {
		return coupang;
	}

	public void setCoupang(String coupang) {
		this.coupang = coupang;
	}

	public String getTving() {
		return tving;
	}

	public void setTving(String tving) {
		this.tving = tving;
	}

	public String getMemberout() {
		return memberout;
	}

	public void setMemberout(String memberout) {
		this.memberout = memberout;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	

}
